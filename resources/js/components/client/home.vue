import navBar from './navbar.vue';
import header from  './header.vue';
import axios from "axios";
import Vue from "vue";
import Toaster from 'v-toaster'
import 'v-toaster/dist/v-toaster.css'
Vue.use(Toaster, {timeout: 5000})
import Spinner from 'vue-simple-spinner';

export default {
  name: "home",
    components:{
      navBar,
      header,
      Spinner
  },
  data(){
      
    return {
            subscriberName:"",
            subscriberPhone:"",
            subscriberEmail:"",
            ourClients:[],
            clientsLoader:false
      }
  },
  methods:{
      getCompanies:function()
      {
          this.clientsLoader = true;
            axios
                .get("api/getCompanies?data_name=companies")
                .then((response) => {
                    if(response.data.status == 200)
                    {
                        this.clientsLoader = false;
                        this.ourClients = response.data.companies;
                    }else{
                        this.$toaster.error(response.data.message)
                    }
                })
                .catch((error) => {
                     this.clientsLoader = false;
                    this.$toaster.error(error.message)
                });

      },
    
      submitSubscriberDetails:function(event)
        {
        
         event.preventDefault();
         let payload = {
                name: this.subscriberName,
                phone:this.subscriberPhone,
                email:this.subscriberEmail
            }

            axios
                .post("api/newSubscriber", payload)
                .then((response) => {

                    if(response.data.status == 201)
                    {
                        this.subscriberName ="";
                        this.subscriberPhone ="";
                        this.subscriberEmail ="";
                        this.$toaster.success("Thank you for subscribing..")

                    }else{
                        this.$toaster.error(response.data.message)
                    }
                })
                .catch((error) => {
                    this.$toaster.error(error.message)
                });
        },
  },
  mounted(){
    this.getCompanies();
  },

  
}
</script>
